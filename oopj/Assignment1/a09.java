class a09{


    public static void main(String [] args)
    {
    
       /* .Write a program to convert state of Byte instance into byte, 
           short, int. long, float and double.   */
    
           Byte byt=14;
    
    
           byte b= byt.byteValue();
           System.out.println("The byte value of byt is b="+b);
    
           short s = byt.shortValue();
           System.out.println("The short value of byt is s="+s);
    
           int i = byt.intValue();
           System.out.println("The int value of byt is i="+i);
    
           long l = byt.longValue();
           System.out.println("The long value of byt is l="+l);
    
           float f = byt.floatValue();
           System.out.println("The float value of byt is f="+f);
    
           double d = byt.doubleValue();
           System.out.println("The double value of byt is d="+d);
    
    
    
    }
    
    
    
    }
    